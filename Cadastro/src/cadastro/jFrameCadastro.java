    private void jButton_cadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_cadastroActionPerformed
     
        String nome = jT_nome.getText();
        String endereco = jT_end.getText();
        String email = jT_email.getText();
        String telefone = jT_telefone.getText();
        String idade = jT_idade.getText();
        String sexo =(s_masc.isSelected()) ? "M":
                (s_Feminino.isSelected())?"F":null;
        
        //"()5565-1365"
       boolean a = telefone.matches("\\d{2}-\\d{4,5}-\\d{4}");
        
            
       boolean b = email.matches("\\w+@\\w+\\.\\w{2,3}");
       
         if(b==true && a==true){
             
              try {
            ConexaoBanco.conecta();
            ConexaoBanco.cadastroCliente(nome, endereco, email,sexo,telefone,idade);//
        } catch (Exception ex) {
            Logger.getLogger(jFrameCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
         }else {
             JOptionPane.showMessageDialog(null, "E-mail ou incorreto!");
           
         }
        
        
          
       
        
        
        
    }//GEN-LAST:event_jButton_cadastroActionPerformed

