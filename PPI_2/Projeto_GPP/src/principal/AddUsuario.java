    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        // TODO add your handling code here:
       Usuario userConect = new Usuario();
       Menuinicio frame1 = new Menuinicio();
        AddUsuario frame2 = new AddUsuario();
        
        String nome = txtnome.getText();
        String login = txtlogin.getText();
        String senha = txtsenha1.getText();
        
        if ((nome.length()>6 || nome.length()>30)|| (login.length()>6 || login.length()>20)) {
    
            
                          if (txtsenha1.getText().toString().equals(txtsenha2.getText().toString()))
                             {
                
                               JOptionPane.showMessageDialog(rootPane, "USUARIO CADASTRADO COM SUCESSO!!");
                    
                              try {
                                  BD_tratamentos.insere(nome, login, senha);
                              } catch (Exception ex) {
                                  Logger.getLogger(AddUsuario.class.getName()).log(Level.SEVERE, null, ex);
                              }
                              
                              
                               frame1.setVisible(true);
                               dispose();
                               
                          }else{
                          JOptionPane.showMessageDialog(rootPane, "SENHAS NAO COMBINAM!!"); 
                                txtsenha1.setText("");
                                txtsenha2.setText("");
                                
                                 frame2.setVisible(true);
                                   dispose();
                             }

        } else {
        JOptionPane.showMessageDialog(rootPane, "QUANTIDADE DE CARACTERES NÃO PERMITIDO!","ATENÇÃO! ",  JOptionPane.WARNING_MESSAGE);        
      
        frame2.setVisible(true);
        dispose();       
        }
     
    }//GEN-LAST:event_btnCadastroActionPerformed

